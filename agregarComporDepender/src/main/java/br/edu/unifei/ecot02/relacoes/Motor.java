package br.edu.unifei.ecot02.relacoes;

public class Motor {
	private String serie;
	private int totalCilindros;
	private int potencia;
	//gets e sets
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public int getTotalCilindros() {
		return totalCilindros;
	}
	public void setTotalCilindros(int totalCilindros) {
		this.totalCilindros = totalCilindros;
	}
	public int getPotencia() {
		return potencia;
	}
	public void setPotencia(int potencia) {
		this.potencia = potencia;
	}
	
}
